public class CharacterAndBooleanVariables {
    public static void main(String[] args) {
        // Character
        char grade = 'A'; // 1 символ
        char number = '1';
        char focus = 67; // таблица ascii
        System.out.println("grade "+grade+" number "+number+" focus "+focus);
        // Boolean
        boolean isEmpty;
        isEmpty = true;
        
        System.out.println("isEmpty: "+isEmpty);
    }
}
